# Задамо значення вибірок
x <- c(0, 2, 3, 4, 6)
n <- c(4, 5, 10, 4, 2)

# Вмістимо два графіка на екрані
par(mfrow = c(2, 1))

# Малюємо графік емпіричної функції розподілу
plot(ecdf(rbind(x, n)), main = "Графік емпіричної функції розподілу")
grid() # Додаємо сітку на фон

# Будуємо полігони частот і відносних частот
plot(x, n / sum(n), type = "l", main = "Полігони частот і відносних частот")
grid()
